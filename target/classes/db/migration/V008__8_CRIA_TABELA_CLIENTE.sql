DROP TABLE IF EXISTS CLIENTE;

-- Criar a tabela
CREATE TABLE CLIENTE (
    ID_CLIENTE BIGINT AUTO_INCREMENT PRIMARY KEY,
    NOME VARCHAR(255) NOT NULL,
